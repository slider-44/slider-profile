	public String getNextPaymentDate() {
		Calendar cal = Calendar.getInstance();
		DateFormat dateFormat = new SimpleDateFormat("ddMMyyyy");
		Date date = new Date();
		
		cal.setTime(date);
		cal.add(Calendar.MONTH, 1);
		date = cal.getTime();
		
		String cd = dateFormat.format(date);
		return "02" + cd.substring(2,8);
		
		
		
	}
	
